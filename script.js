
function acao(tipo) {
  if (tipo === 'doacoes') {
    alert('Você escolheu saber mais sobre doações!');
  } else if (tipo === 'doar') {
    alert('Você será direcionado para realizar uma doação.');
  } else if (tipo === 'voluntario') {
    alert('Obrigado pelo interesse em ser voluntário!');
  }
}
